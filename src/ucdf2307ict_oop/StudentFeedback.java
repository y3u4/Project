    private void submitFeedbackButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitFeedbackButtonActionPerformed
        // Check if feedback has been submitted for this appointment
        if (isFeedbackSubmitted()) {
            JOptionPane.showMessageDialog(this, "Feedback has already been submitted for this appointment. Please choose another one.", "Feedback Submitted", JOptionPane.WARNING_MESSAGE);
            StudentAppointment sa = new StudentAppointment(studentID);
            sa.setVisible(true);
            this.dispose();
        } else {
            // Get the feedback entered by the user
            String feedback = feedbackTextArea.getText().trim();
            
            // Check if feedback is empty
            if (feedback.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please provide feedback before submitting.", "Feedback Required", JOptionPane.WARNING_MESSAGE);
            } else {
                // Save the feedback to StudentFeedback.txt
                try (FileWriter fw = new FileWriter("studentFeedback.txt", true);
                    BufferedWriter bw = new BufferedWriter(fw)) {
                    // Append the feedback with day, time, lecturer
                    bw.write(studentID + "," + day + "," + time + "," + lecturer + "," + feedback);
                    bw.newLine(); // Write each entry in a new line
                    JOptionPane.showMessageDialog(this, "Thank you for your feedback!", "Feedback Submitted", JOptionPane.INFORMATION_MESSAGE);
                    feedbackTextArea.setText(""); // Clear the feedback text area
                    
                    // Return to the StudentAppointment page
                    StudentAppointment sa = new StudentAppointment(studentID);
                    sa.setVisible(true);
                    this.dispose();
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "An error occurred while submitting feedback.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_submitFeedbackButtonActionPerformed

