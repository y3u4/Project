    // checks if feedback has already been submitted for the given appointment
    private boolean isFeedbackSubmitted() {
        File feedbackFile = new File("studentFeedback.txt");
        try (FileReader fr = new FileReader(feedbackFile);
            BufferedReader br = new BufferedReader(fr)) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 5 && 
                    parts[1].trim().equalsIgnoreCase(day.trim()) && 
                    parts[2].trim().equalsIgnoreCase(time.trim()) && 
                    parts[3].trim().equalsIgnoreCase(lecturer.trim())) {
                    return true; // Feedback already submitted
                }

            }
        } catch (IOException e) {
            e.getMessage();
        }
        return false; // No feedback submitted yet
    }

